LIST P=16F877A
PORTA EQU h'05'
PORTB EQU h'06'
STATUS EQU h'03'
TRISA EQU h'85'
TRISB EQU h'86'
ADCON1 EQU h'9F'

CLRF PORTB
BSF STATUS,5
CLRF TRISB
MOVLW h'06'
MOVWF ADCON1
MOVLW h'FF'
MOVWF TRISA
BCF STATUS,5

TEST
BTFSS PORTA,0
GOTO TEST1
MOVLW h'03'
MOVWF PORTB

TEST1
BTFSS PORTA,1
GOTO TEST2
MOVLW h'0C'
MOVWF PORTB

TEST2
BTFSS PORTA,2
GOTO TEST3
MOVLW h'30'
MOVWF PORTB

TEST3
BTFSS PORTA,3
GOTO TEST
MOVLW h'C0'
MOVWF PORTB
GOTO TEST 
END
