LIST P=16F877A
INCLUDE "P16F877A.INC"
SAYAC1 EQU h'0C'
SAYAC2 EQU h'0D'
SAYAC3 EQU h'0E'
CLRF PORTD
BCF STATUS,0
BSF STATUS,5
CLRF TRISD
MOVLW h'06'
MOVWF ADCON1
MOVLW h'FF'
MOVWF TRISA
BCF STATUS,5

BASLANGIC 
MOVLW h'01'
MOVWF PORTD
CALL GECIKME
SOL
CALL GECIKME
RLF PORTD,F
BTFSS PORTD,7
GOTO SOL
SAG
CALL GECIKME
RRF PORTD,F
BTFSS PORTD,0
GOTO SAG
GOTO SOL

GECIKME

MOVLW h'8F'
MOVWF SAYAC1
DONGU1
MOVLW h'8F'
MOVWF SAYAC2
DONGU2
MOVLW h'8F'
MOVWF SAYAC3
DONGU3
DECFSZ SAYAC3,F
GOTO DONGU3
DECFSZ SAYAC2,F
GOTO DONGU2
DECFSZ SAYAC1,F
GOTO DONGU1
RETURN
END
