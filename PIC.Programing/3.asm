LIST P=16F877A
INCLUDE "P16F877A.INC"
SAYAC1 EQU h'0C'
SAYAC2 EQU h'0D'
SAYAC3 EQU h'0E'
CLRF PORTD
BCF STATUS,0
BSF STATUS,5
CLRF TRISD
MOVLW h'06'
MOVWF ADCON1
MOVLW h'FF'
MOVWF TRISA
BCF STATUS,5
BASLANGIC
MOVLW h'01'
MOVWF PORTD

TEKRAR
CALL GECIKME
TEST
BTFSS PORTA,0
GOTO TEST
RLF PORTD,F
BTFSS STATUS,0
GOTO TEKRAR
BCF STATUS,0
GOTO BASLANGIC

GECIKME
MOVLW h'FF'
MOVWF SAYAC1
DONGU1
MOVLW h'FF'
MOVWF SAYAC2
DONGU2
MOVLW h'FF'
MOVWF SAYAC3
DONGU3
DECFSZ SAYAC3,F
GOTO DONGU3
DECFSZ SAYAC2,F
GOTO DONGU2
DECFSZ SAYAC1,F
GOTO DONGU1
RETURN
END
